function expandRangesInData(obj) {
    // Check if the current object is an array
    if (Array.isArray(obj)) {
        return obj.flatMap(item => expandRangesInData(item));
    }

    // If the current object is an object, iterate through its properties
    if (typeof obj === 'object' && obj !== null) {
        for (const key in obj) {
            if (key === 'mcc' && Array.isArray(obj[key])) {
                // Process the 'mcc' array
                obj[key] = obj[key].flatMap(number => {
                    if (typeof number === 'string' && number.includes('-')) {
                        const [start, end] = number.split('-').map(num => parseInt(num, 10));
                        return Array.from({ length: end - start + 1 }, (_, i) => String(start + i).padStart(4, '0'));
                    }
                    return number;
                });
            } else {
                // Recursively process nested objects
                expandRangesInData(obj[key]);
            }
        }
    }

    return obj;
}

